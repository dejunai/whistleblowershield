# Ingest Tool Design

**Project:** WhistleblowerShield Core (`ws-core`)
**File:** `documentation/ingest-tool-design.md`
**Status:** Design specification — implementation pending

---

## Overview

The ingest tool is a PHP-based utility for importing structured legal data
into `ws-core` CPTs from JSON files generated by AI-assisted research (e.g.
Gemini, Claude AI). It is not a one-time migration tool — it is a repeatable,
auditable pipeline designed to run across multiple jurisdictions and data
batches over the life of the project.

The tool is deliberately simple: regex for predictable structures, direct text
writes for prose fields, and a proposed terms log for everything that needs
human review. No complex translation matrix.

---

## JSON File Structure

Every ingest file must contain two top-level keys: `meta` and `records`.

### `meta` block (required)

The `meta` block is read first, before any records are processed. It provides
the run context the ingest tool needs to stamp every record consistently and
to build the proposed terms blacklist before touching any content.

```json
{
  "meta": {
    "source_method":       "ai_assisted",
    "source_name":         "Gemini",
    "jurisdiction_id":     "CA",
    "generated_date":      "2026-03-18",
    "generated_by":        "Gemini 1.5 Pro",
    "record_count":        15,
    "new_terms_proposed": [
      {
        "taxonomy":  "ws_disclosure_type",
        "term":      "Proposed Term Label",
        "notes":     "Gemini's reasoning for why this term was included.",
        "seen_in":   [ "CA-1102.5", "CA-8547" ]
      }
    ]
  },
  "records": []
}
```

### `records` array

An array of statute objects. See `build-json-directive.md` for the full
field schema Gemini is directed to produce.

---

## Processing Pipeline

The ingest tool executes in the following strict sequence:

### Phase 1 — Header parse

1. Read and validate the `meta` block.
2. Load the existing `proposed-terms-log.json` from the documentation directory.
3. Merge any new proposed terms from `meta.new_terms_proposed` into the log,
   appending to `seen_in` arrays for terms already present rather than
   creating duplicates.
4. Build a local blacklist of all proposed term values for use in Phase 3.
5. Save the updated log before processing any records.

### Phase 2 — Pre-flight report

Before processing records, output a summary:

- Total records in batch
- `source_method` and `source_name` that will be stamped on all records
- Count of proposed new terms found in header
- List of proposed terms with taxonomy type and `seen_in` references
- Prompt for confirmation before proceeding (interactive mode only)

### Phase 3 — Record processing

For each record:

1. Create a new WP post of the appropriate CPT (`jx-statute` for statute records).
2. Call `ws_set_source_method( $post_id, WS_SOURCE_AI_ASSISTED )` from header meta.
3. Call `ws_set_source_name( $post_id, $meta['source_name'] )` from header meta.
4. Map each JSON field to its corresponding ACF post meta key (see Field Map below).
5. For array fields (e.g. `disclosure_types`), use `wp_set_object_terms()` for
   taxonomy fields and serialize or repeat for repeater fields.
6. For any field value matching a blacklisted proposed term:
   - Remove the term from the field value entirely.
   - Log the removal: which post, which field, which term was removed.
   - Do NOT write a partial or placeholder value — omit cleanly.
7. After all fields are written, check plain English fields (see below).
8. Set `verification_status` to `unverified` — never `verified` on ingest.

### Phase 4 — Plain English field audit

After each record is written, inspect every plain English eligible field.
If any such field contains text:

- Set `has_plain_english` to `true` on the post.
- Add the post to the plain English review queue.

Output per record:
- Count of plain English fields populated
- List of each populated plain English field by name

Output per batch:
- Total records entering the plain English review queue
- Aggregated list of all affected fields across the batch

### Phase 5 — Post-run report

After all records are processed, write a run report covering:

- Records created successfully
- Records skipped or failed (with reasons)
- Proposed terms removed (post title, field name, term value)
- Plain English queue entries
- Path to updated `proposed-terms-log.json`

---

## Proposed Terms Log

**File:** `documentation/proposed-terms-log.json`

Persistent JSON file maintained across all ingest runs. The ingest tool
reads this file at the start of every run and writes it back after Phase 1
before processing any records.

### Schema

```json
{
  "proposed_terms": [
    {
      "taxonomy":    "ws_disclosure_type",
      "term":        "Proposed Term Label",
      "notes":       "Gemini's reasoning for inclusion.",
      "seen_in":     [ "CA-1102.5", "CA-8547" ],
      "status":      "pending",
      "resolved_on": null,
      "resolution":  null
    }
  ]
}
```

### `status` values

| Value      | Meaning                                              |
|------------|------------------------------------------------------|
| `pending`  | Awaiting human review                                |
| `approved` | Term added to taxonomy — remediation pass needed     |
| `rejected` | Term will not be added — affected records are clean  |
| `mapped`   | Term mapped to an existing taxonomy term             |

### `resolution` field

Free text. When status is set to `approved`, `rejected`, or `mapped`,
record the decision rationale here for future reference.

