## Source Method & Verification Behavior

All ingest records receive:

- `source_method` → value from `meta.source_method` (e.g. `ai_assisted`)
- `source_name`   → value from `meta.source_name` (e.g. `Gemini`)
- `verification_status` → always `unverified` on ingest, regardless of source
- `needs_review`  → `false` on ingest — set manually by admin if needed

Records cannot reach `verification_status = verified` until:
1. `source_name` is populated (always true for ingest records)
2. A human editor manually sets the flag after reviewing the content

The plain English review queue is independent of verification status —
records can be verified before or after plain English review.

---

## Automation vs Interactive Mode

| Mode          | Behavior                                                      |
|---------------|---------------------------------------------------------------|
| Interactive   | Pre-flight report shown, confirmation required before Phase 3 |
| Automated     | Pre-flight report logged to file, processing begins immediately |

Automated mode is appropriate for recurring feed imports (`feed_import`).
Interactive mode is recommended for all first-time batch imports.

---

## Key Architectural Constraints

- **Shortcodes are presentation layer only.** The ingest tool writes directly
  to post meta via WP/ACF APIs — never through shortcodes.
- **`ws_set_source_method()` and `ws_set_source_name()` are immutable.**
  Once written they cannot be overwritten. The ingest tool must call them
  before any subsequent save hooks fire.
- **Proposed terms are removed, not flagged.** The record is written clean.
  The proposed terms log carries the full audit trail of what was removed
  and where.
- **Plain English fields are written as-is from Gemini output.** No
  sanitization beyond standard WP escaping. The plain English review process
  handles quality control downstream.

